package ScientificDocumentClasses;

public class Journal {
	private String PublisherName;
	private String PublisherLocation;
	private String PublisherVenue;
	private String JournalName;
	/**
	 * @return the publisherName
	 */
	public String getPublisherName() {
		return PublisherName;
	}
	/**
	 * @param publisherName the publisherName to set
	 */
	public void setPublisherName(String publisherName) {
		PublisherName = publisherName;
	}
	/**
	 * @return the publisherLocation
	 */
	public String getPublisherLocation() {
		return PublisherLocation;
	}
	/**
	 * @param publisherLocation the publisherLocation to set
	 */
	public void setPublisherLocation(String publisherLocation) {
		PublisherLocation = publisherLocation;
	}
	/**
	 * @return the publisherVenue
	 */
	public String getPublisherVenue() {
		return PublisherVenue;
	}
	/**
	 * @param publisherVenue the publisherVenue to set
	 */
	public void setPublisherVenue(String publisherVenue) {
		PublisherVenue = publisherVenue;
	}
	/**
	 * @return the journalName
	 */
	public String getJournalName() {
		return JournalName;
	}
	/**
	 * @param journalName the journalName to set
	 */
	public void setJournalName(String journalName) {
		JournalName = journalName;
	}

}
